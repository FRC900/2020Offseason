void runTest(const std::shared_ptr<costmap_2d::Costmap2DROS> &costmap,
			const geometry_msgs::TransformStamped &pathToMapTransform,
			const std::vector<unsigned int> &potentials,
			const std::vector<SegmentState<CostType>> &xStates,
			const std::vector<SegmentState<CostType>> &yStates,
			ObstacleCostImplType type, const char *name)
{
	ObstacleCost<CostType> oc(type);
	std::vector<CostType> arcSegCosts;

    auto t1 = high_resolution_clock::now();
	for (int i = 0; i < 100000; i++)
	{
		oc.calculate(arcSegCosts, costmap,
				pathToMapTransform, potentials,
				xStates, yStates);
	}
    auto t2 = high_resolution_clock::now();
    const duration<double, std::milli> ms_double = t2 - t1;

	for (const auto asc : arcSegCosts)
		ROS_INFO_STREAM(" segCost[] = " << asc);
    ROS_INFO_STREAM(__FUNCTION__ << name << " : " << ms_double.count() << " mSec");

}
